// Example:
// C-String: printing characters such as ', ", \, and / 


#include <cstring>  
#include <iostream>
using namespace std;


int main()
{
 // check 
  cout << "printing single, double quotes: " << "\'" << ", " << "\"" << endl;
  cout << "printing back slash           : " << "\\" << endl;
  cout << "printing forward slash        : " << "/" << endl;

  cout << endl;
  return 0;
}
